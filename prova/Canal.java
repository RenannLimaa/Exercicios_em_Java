package prova;

public class Canal {
    private String nome;
    private boolean HD;
    private int numero;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public boolean isHD() {
        return HD;
    }
    public void setHD(boolean hD) {
        HD = hD;
    }
    public int getNumero() {
        return numero;
    }
    public void setNumero(int numero) {
        this.numero = numero;
    }
}
